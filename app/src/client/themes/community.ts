import type { Theme } from './type'

type ThemeSeed = {
  id: string
  name: string
  dark: boolean
  background: string
  foreground: string
  primary: string
  secondary?: string
  muted?: string
  accent?: string
  success?: string
  warning?: string
  danger?: string
  primaryForeground?: string
  successForeground?: string
  warningForeground?: string
  dangerForeground?: string
  tagColors?: Partial<Theme['tagColors']>
}

const darkSurface = '36 40 48'
const darkMuted = '44 49 58'
const darkAccent = '62 68 81'
const lightSurface = '229 231 235'
const lightMuted = '243 244 246'
const lightAccent = '209 213 219'

const createTheme = (seed: ThemeSeed): Theme => {
  const primaryForeground =
    seed.primaryForeground ?? (seed.dark ? '15 23 42' : '255 255 255')
  const success = seed.success ?? (seed.dark ? '74 222 128' : '22 163 74')
  const warning = seed.warning ?? (seed.dark ? '250 204 21' : '202 138 4')
  const danger = seed.danger ?? (seed.dark ? '248 113 113' : '220 38 38')

  return {
    id: seed.id,
    name: seed.name,
    dark: seed.dark,
    tagColors: {
      string: seed.tagColors?.string ?? success,
      list: seed.tagColors?.list ?? (seed.dark ? '244 114 182' : '190 24 93'),
      set: seed.tagColors?.set ?? seed.primary,
      zset: seed.tagColors?.zset ?? (seed.dark ? '168 85 247' : '147 51 234'),
      hash: seed.tagColors?.hash ?? warning,
      stream: seed.tagColors?.stream ?? danger,
    },
    baseColors: {
      background: seed.background,
      foreground: seed.foreground,
      primary: seed.primary,
      primaryForeground,
      secondary: seed.secondary ?? (seed.dark ? darkSurface : lightSurface),
      muted: seed.muted ?? (seed.dark ? darkMuted : lightMuted),
      accent: seed.accent ?? (seed.dark ? darkAccent : lightAccent),
      success,
      successForeground:
        seed.successForeground ?? (seed.dark ? '15 23 42' : '240 253 244'),
      warning,
      warningForeground:
        seed.warningForeground ?? (seed.dark ? '23 23 23' : '254 252 232'),
      danger,
      dangerForeground:
        seed.dangerForeground ?? (seed.dark ? '255 255 255' : '254 242 242'),
    },
  }
}

